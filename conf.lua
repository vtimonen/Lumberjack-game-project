function love.conf(t)
    t.window.title = "Lumberjack" -- window title
    t.window.width = 500          -- width
    t.window.height = 500         -- height
    t.window.resizable = true     -- allow resizing
    t.window.vsync = 1            -- enable vsync
    t.window.fullscreen = false   -- start in windowed mode
end
